package Basic_structure_and_Syntax;
public class Datatypes {
public static void main(String[] args) {
  System.out.println("data types in java");
  byte myByte = 100;
 System.out.println("byte value: " + myByte);
  short myShort = 500;
  System.out.println("Short value:" + myShort);
  int myInt = 600;
  System.out.println("Int value:" +myInt);
  long myLong = 150000000L;
  System.out.println("Long value:" + myLong);
  float myFloat = 6000000.0f;
  System.out.println("float value:" + myFloat);
  double myDouble = 3.33333333333333d;
  System.out.println("double value:" + myDouble);
  float myscientificfloat = 1.24e4f;// scientific notation for float
  System.out.println("scientific notation double value " + myscientificfloat);
  double myScientificDouble = 1.67e4d;// scientific notaion for double
 System.out.println("scientific notation double value:" + myScientificDouble);
 boolean myBoolean = true;
 System.out.println("boolean value:" + myBoolean);
 char myChar = 'A';
 System.out.println("char value:" + myChar); 
 char myVar1 = 123, myVar2 = 125, myVar3 = 67;
 System.out.println(myVar1);
 System.out.println(myVar2);
 System.out.println(myVar3);
 String myNull;
 myNull = null;
 System.out.println("null value: " + myNull);
 var myVarNum = 5;  //int
 var myVarDouble = 9.98; // double 
 var myVarChar = 'A';  //char 
 var myVarBoolean = true; // boolean
 var myVarString = "hi"; //String
System.out.println("Var Num Value: " + myVarNum);
System.out.println("Var Double Value: " + myVarDouble);
System.out.println("Var Char Value: " + myVarChar);
System.out.println("Var Boolean Value: " + myVarBoolean);
System.out.println("Var String Value: " + myVarString);

 

}
}